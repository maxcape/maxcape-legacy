                    <h2>Clan Rules</h2>
                    <hr>
                    <p><b>The following are the official rules of the Iron Clan. If you have any questions about whether something is allowed or not, please ask a
                        high ranking person in the clan chat about it.</b></p>

                    <ul>
                        <li>
                            <p>If you're looking to join the Clan, you need to make a new account with a name "Iron _____".</p>

                            <ul>
                                <li>Come to World 28 (Members) and find someone who can invite you or Join the clan chat "Iron Dragon"</li>
                            </ul>
                        </li>
                        <li><p>No use of the Grand Exchange is allowed and no trading outside of the clan.</p>
                            <ul>
                                <li>The only exception to this rule is that you <b>are</b> allowed to be given a bond from an outside account. Preferably, this account should be your main account.</li>
                                <li>You may choose to trade with people within the clan, but it is completely optional to do so.</li>
                                <li>Screenshots of your Grand Exchange history may be asked for to verify an item was receieved as a drop. If available, a screenshot of the drop is also helpful.
                                    <ul>
                                        <li><p>Grand Exchange History should not have all five slots filled with bonds.</p>
                                            <ul>
                                                <li>Have another player trade a bond through your main in order to go around this.</li>
                                            </ul>
                                        </li>
                                        <li>It is recommended to post rare drops to the clan <a href="http://www.reddit.com/r/ironClan" target="_blank">Subreddit</a>. This is not only for proof, but also to show off and have fun.</li>
                                    </ul>
                                </li>

                            </ul>
                        </li>
                        <li><p>All group content (minigames, bossing, Dungeoneering, etc) can only be done solo or with Iron Clan members with the exception of warbands, which is <b>not allowed</b> to be used.</p></li>
                        <li>Treasure Hunter is <b>not</b> allowed.</li>
                        <li>
                            <p>All Iron Members need to have their Adventurer's Log turned on. It may be used to prove they got the loot and/or gear you may get from drops.</p>

                            <ul>
                                <li>Please <b>turn off</b> Level-ups on your Adventurer's Log settings. This can be found by pressing Escape->Game Settings.</li>
                            </ul>
                        </li>
                        <li><p>All Auras available through the Loyalty Programme are allowed.</p>
                            <ul>
                                <li><p>Experience boosting items from paid promotions (game cards, treasure hunter, etc) are <b>not</b> allowed. This includes the Enlightenment Aura and Crystal Amulet.</p>
                            </ul>
                        </li>
                        <li><p>You are allowed to do Holiday Events when they're available.</p></li>
                        <li><p>PKing is allowed, but you <b>are not</b> allowed to pick up the loot.</p></li>
                        <li><p>No gambling or staking.</p></li>
                        <li><p>You're allowed to buy from all in-game shops.</p></li>
                        <li><p>World Events are allowed.</p></li>
                        <li><p>Daily Challenges <b>are</b> allowed, along with the Challenge Bags and Experience Lamps. </p>
                            <ul>
                                <li>Treasure Hunter keys earned through Daily Challenges are still <b>not</b> allowed.</li>
                            </ul>
                        </li>
                        <li><p>Cosmetic Overrides from Solomons General Store are allowed.</p>
                            <ul>
                                <li>Bank Boosters are allowed.</li>
                                <li>Legendary Pets are <b>not</b> allowed.</li>
                            </ul>
                        </li>
                    </ul>

                    <p><b>Disclaimer: </b>These rules are subject to change and addition. Please check back occasionally to stay up to date!</p>
